    @Override
    public void showListOnTable(Integer month, Integer year) {
        DefaultTableModel model = (DefaultTableModel) tblLuong.getModel();
        model.setRowCount(0);

        try {
            listLuongThang = luongdao.getBySql("select * from luong where MONTH(thang)=? and year(thang)=?", month, year);
            for (Luong luong : listLuongThang) {
                for (NhanVien nv : listNV) {
                    if (luong.getMaNV().equalsIgnoreCase(nv.getMaNV())) {
                        tinhTP(nv);

                        Cong = ccdao.getByKeyword(nv.getMaNV(), month, year);
                        if (Cong < 25) {
                            thucLanh = (nv.getLuong() / 25) * Cong + thuong + phat;
                            setOnOffTxtFields(false);

                        } else {
                            setOnOffTxtFields(true);
                            thucLanh = (nv.getLuong() / 25) * Cong + luong.getBHXH()
                                    + luong.getBHYT() + luong.getPCCOM() + luong.getPCKhac() + luong.getPCXang() + thuong + phat;
                        }
                        Object[] row = {
                            false,
                            luong.getMaNV(),
                            nv.getHoTen(),
                            nv.getMaPB() == 1 ? "Phòng kế toán" : "Phòng bán hàng",
                            nv.getMaCV() == 1 ? "Trưởng phòng" : "Nhân viên",
                            df.format(nv.getLuong()),
                            df.format(thucLanh)};
                        model.addRow(row);
                        thucLanh = 0.0;
                    }

                }

            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
            e.printStackTrace();
        }
    }

    @Override
    public void createTable() {
        DefaultTableModel model = (DefaultTableModel) tblLuong.getModel();
        model.setRowCount(0);
        for (NhanVien nv : listNV) {
            int dem = 0;
            Luong luong = new Luong(0, nv.getMaNV(), XDate.toDate(String.valueOf(txtThoiGian.getDate().getYear() + 1900)
                    + "-" + String.valueOf(txtThoiGian.getDate().getMonth() + 1) + "-" + "01", "yyyy-MM-dd"),
                    0, 0, 0, 0, 0);
            showListOnTable(txtThoiGian.getDate().getMonth() + 1, txtThoiGian.getDate().getYear() + 1900);
            if (tblLuong.getRowCount() == 0) {
                luongdao.create(luong);

            } else {
                for (int i = 0; i < tblLuong.getRowCount(); i++) {
                    if (nv.getMaNV().equalsIgnoreCase(String.valueOf(tblLuong.getValueAt(i, 1)))) {
                        dem += 1;
                    }
                }
                if (dem == 0) {
                    luongdao.create(luong);

                }

            }

        }
        showListOnTable(txtThoiGian.getDate().getMonth() + 1, txtThoiGian.getDate().getYear() + 1900);
        updateStatus();

    }

