    @Override
    public void tinhTP(NhanVien nv) {
        thuong = 0.0;
        phat = 0.0;

        listTVKL = tvkldao.getByMaNV(nv.getMaNV(), txtThoiGian.getDate().getMonth() + 1, txtThoiGian.getDate().getYear() + 1900);
        try {
            for (ThuongVaKyLuat tvkl : listTVKL) {

                if (tvkl.getLoai()) {
                    thuong += tvkl.getSoTien();
                } else {
                    phat += tvkl.getSoTien();
                }
            }
        } catch (Exception e) {
        }

    }

