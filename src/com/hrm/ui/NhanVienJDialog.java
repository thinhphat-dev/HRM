    @Override

    public void edit() {
        String manv = (String) tblNhanVien.getValueAt(this.row, 0);
        NhanVien nv = dao.getById(manv);
        this.setForm(nv);
        tabs.setSelectedIndex(0);
        this.updateStatus();

    }

    @Override
    public void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNV());
        txtHoTen.setText(nv.getHoTen());
        txtMatKhau.setText(nv.getMatKhau());
        txtHoTen.setText(nv.getHoTen());
        txtDiaChi.setText(nv.getDiaChi());
        txtNgay.setDate(nv.getNgaySinh());
        if (nv.getGioiTinh()) {
            rdoNam.setSelected(true);
            rdoNu.setSelected(false);
        } else {
            rdoNam.setSelected(false);
            rdoNu.setSelected(true);
        }
        txtCCCD.setText(nv.getCCCD());
        txtQueQuan.setText(nv.getQueQuan());
        cboPhongBan.setSelectedIndex(nv.getMaPB() == 1 ? 0 : 1);
        if (nv.getMaCV() == 1) {
            rdoTruongPhong.setSelected(true);

        } else {
            rdoNhanVien.setSelected(true);
        }
        txtLuong.setText(String.valueOf(nv.getLuong()));
        txtMatKhau.setText(nv.getMatKhau());
        txtMatKhau2.setText(nv.getMatKhau());

        txtSDT.setText(nv.getSDT());
        txtEmail.setText(nv.getEmail());
        if (nv.getHinh() != null) {
            lblHinh.setToolTipText(nv.getHinh());
            lblHinh.setIcon(XImage.read(nv.getHinh(), lblHinh));
        }
    }

    @Override
    public NhanVien getForm() {
        NhanVien entity = new NhanVien();
        entity.setMaNV(txtMaNV.getText());
        entity.setHoTen(txtHoTen.getText());
        entity.setDiaChi(txtDiaChi.getText());
        entity.setSDT(txtSDT.getText());
        entity.setNgaySinh(txtNgay.getDate());
        if (rdoNam.isSelected()) {
            entity.setGioiTinh(true);
        } else {
            entity.setGioiTinh(false);
        }
        entity.setCCCD(txtCCCD.getText());
        entity.setQueQuan(txtQueQuan.getText());
        entity.setHinh(lblHinh.getToolTipText());
        entity.setMaPB(cboPhongBan.getSelectedIndex() + 1);
        entity.setMaCV(rdoTruongPhong.isSelected() ? 1 : 2);
        entity.setLuong(Double.valueOf(txtLuong.getText()));
        entity.setMatKhau(txtMatKhau.getText());
        entity.setEmail(txtEmail.getText());

        return entity;
    }

    @Override
    public void updateStatus() {
        boolean edit = (this.row >= 0); //flase
        boolean first = (this.row == 0);//false
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);//flase
        // Trạng thái form
        txtMaNV.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        // Trạng thái điều hướng
        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit);
        btnNext.setEnabled(edit);
        btnLast.setEnabled(edit && !last);
    }

    @Override
    public boolean isValidated() {
        NhanVien nv = this.getForm();
        if (nv.getMaNV().length() == 0) {
            MsgBox.alert(this, "Không để trống mã nhân viên!");
        } else if (nv.getHoTen().length() == 0) {
            MsgBox.alert(this, "Không để trống họ và tên nhân viên!");
        } else if (nv.getSDT().length() == 0) {
            MsgBox.alert(this, "Không để trống SDT nhân viên!");
        } else if (nv.getCCCD().length() == 0) {
            MsgBox.alert(this, "Không để trống CCCD nhân viên!");
        } else if (nv.getDiaChi().length() == 0) {
            MsgBox.alert(this, "Không để trống địa chỉ nhân viên!");
        } else if (nv.getQueQuan().length() == 0) {
            MsgBox.alert(this, "Không để trống quê quán nhân viên!");
        } else if (nv.getNgaySinh().toString().equals("")) {
            MsgBox.alert(this, "Không để trống ngày sinh nhân viên!");
        } else if (nv.getLuong().equals(null)) {
            MsgBox.alert(this, "Không để trống lương nhân viên!");
        } else if (nv.getMatKhau().equals("")) {
            MsgBox.alert(this, "Mật khẩu không được để trống!");
        } else if (nv.getHinh().equals("")) {
            MsgBox.alert(this, "Hình nhân viên không được để trống!");
        } else if (!nv.getMatKhau().equals(txtMatKhau2.getText())) {
            MsgBox.alert(this, "Xác nhận mật khẩu không đúng!");
        } else {
            return true;
        }
        return false;
    }
