    @Override
    public void fillTable() {
        List<NhanVien> list = dao.getAll();
        this.showListOnTable(list);
    }

    @Override
    public void showListOnTable(List<NhanVien> list) {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);

        for (NhanVien e : list) {
            Object[] row = {e.getMaNV(), e.getHoTen(), e.getSDT(), XDate.toString(e.getNgaySinh(), "dd/MM/yyyy"),
                e.getGioiTinh() ? "Nam" : "Nữ", e.getMaPB() == 1 ? "Phòng kế toán" : "Phòng bán hàng",
                e.getMaCV() == 1 ? "Trưởng phòng" : "Nhân viên", e.getEmail()};

            model.addRow(row);
        }
    }

