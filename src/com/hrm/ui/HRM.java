    @Override
    public void time() {
        new Thread() {
            @Override
            public void run() {
                while (true) {
                    SimpleDateFormat df = new SimpleDateFormat();
                    //Lấy thời gian
                    df.applyPattern("hh:mm:ss a");
                    //Lấy thời gian thực
                    Date d = new Date();
                    //chuyển đổi thời gian hiện tại sang dạng Chuỗi ký tự
                    String sThoiGian = df.format(d);
                    lblDongHo.setText(sThoiGian);
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                    }
                }
            }
        }.start();

    }

    @Override
    public void darkMode(int status) {
        if (status == 1) {
            lblTrump.setBackground(Color.darkGray);
            toolBar.setBackground(Color.gray);
        } else {
            Color toolbarcolor = new Color(0, 49, 64);
            Color centercolor = new Color(89, 168, 120);
            lblTrump.setBackground(centercolor);
            toolBar.setBackground(toolbarcolor);
        }

    }

