    @Override
    public void openNhanVien() {
        if (Auth.isLogin()) {
            if (Auth.isManager().equalsIgnoreCase("Trưởng phòng kế toán") || Auth.isManager().equalsIgnoreCase("Nhân viên")) {
                MsgBox.alert(this, "Bạn không có quyền xem thông tin nhân viên!!!");
                return;
            }
            new NhanVienJDialog(this, true).setVisible(true);
        } else {
            MsgBox.alert(this, "Vui lòng đăng nhập!");
        }
    }

